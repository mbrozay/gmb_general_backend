package com.gmbestablished.gmb_backend_general_rest.pojo;

public class Testimonials {
	
	private String testimonialFrom;
	private String testimonialBody;
	private String testimonialFromWebsiteUrl;
	public String getTestimonialFrom() {
		return testimonialFrom;
	}
	public void setTestimonialFrom(String testimonialFrom) {
		this.testimonialFrom = testimonialFrom;
	}
	public String getTestimonialBody() {
		return testimonialBody;
	}
	public void setTestimonialBody(String testimonialBody) {
		this.testimonialBody = testimonialBody;
	}
	public String getTestimonialFromWebsiteUrl() {
		return testimonialFromWebsiteUrl;
	}
	public void setTestimonialFromWebsiteUrl(String testimonialFromWebsiteUrl) {
		this.testimonialFromWebsiteUrl = testimonialFromWebsiteUrl;
	}
	
	

}
